    private boolean validaCamposVazios(String codigo, String descricao, String auxPreco) {
        if(codigo.equals("")){
            return false;
        }

        if(descricao.equals("")){
            return false;
        }

        if(auxPreco.equals("")){
            return false;
        }

        return true;
    }
