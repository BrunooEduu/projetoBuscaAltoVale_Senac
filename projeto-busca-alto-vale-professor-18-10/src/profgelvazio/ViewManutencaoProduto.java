    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int codigoProduto = Integer.parseInt(edtCodigo.getText());

        if (vendadb.procurarProdutoNaVenda(codigoProduto)) {
            JOptionPane.showMessageDialog(null, "Produto já foi utilizado na venda, não pode ser excluído!");
        } else {
            if (produtodb.excluirProduto(codigoProduto)) {
                JOptionPane.showMessageDialog(null, "Produto excluído com sucesso!");

                limpaCampos();

                habilitaCampos(false);

                edtCodigo.requestFocus();
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // Seta os dados da tela no modelo de produto
        String auxCodigo = edtCodigo.getText();
        String descricao = edtDescricao.getText();
        String auxPreco = edtPreco.getText();

        // valida campos vazios
        if(validaCamposVazios(auxCodigo, descricao, auxPreco)){
            int codigo = Integer.parseInt(auxCodigo);
            Double preco = Double.parseDouble(auxPreco);

            ModelProduto produto = new ModelProduto(codigo, descricao, preco);

            ModelProduto produtoBanco = produtodb.getProduto(codigo);
            if (produtoBanco.getCodigo() > 0) {
                if(produtodb.gravarAlteracaoProduto(produto)){
                    JOptionPane.showMessageDialog(null, "Produto alterado com sucesso!");

                    limpaCampos();

                    habilitaCampos(false);

                    edtCodigo.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao gravar alteração do produto!");
                }
            } else {
                if(produtodb.gravarInsercaoProduto(produto)){
                    JOptionPane.showMessageDialog(null, "Produto inserido com sucesso!");

                    limpaCampos();

                    habilitaCampos(false);

                    edtCodigo.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao inserir produto!");
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Existem campos nao preenchidos!");
        }
    }//GEN-LAST:event_btnGravarActionPerformed

