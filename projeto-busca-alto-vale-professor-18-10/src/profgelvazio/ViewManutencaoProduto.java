    private void edtCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edtCodigoKeyPressed
        if ((evt.getKeyCode() == KeyEvent.VK_ENTER)
                || (evt.getKeyCode() == KeyEvent.VK_TAB)) {

            if (edtCodigo.getText().equals("")) {

                edtCodigo.setText(String.valueOf(produtodb.getUltimoCodigo("tbproduto", "procodigo")));

                habilitaCampos(true);

                edtDescricao.requestFocus();
            } else {
                int codigo = Integer.parseInt(edtCodigo.getText());
                ModelProduto produtoBanco = produtodb.getProduto(codigo);
                if (produtoBanco.getCodigo() > 0) {
                    // se existe produto no banco de dados
                    // seta os dados na tela
                    edtDescricao.setText(produtoBanco.getDescricao());
                    String auxPreco = "" + produtoBanco.getPreco();
                    edtPreco.setText(auxPreco);
                    habilitaCampos(true);
                    edtDescricao.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(null, "Produto não existe!");
                    habilitaCampos(false);
                    edtCodigo.requestFocus();
                }
            }
        }
    }//GEN-LAST:event_edtCodigoKeyPressed

